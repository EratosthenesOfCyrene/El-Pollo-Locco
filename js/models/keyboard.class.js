
/**
 * @typedef {Object} Keyboard
 * @property {boolean} LEFT - Checks if the left arrow key is pressed.
 * @property {boolean} RIGHT - Checks if the right arrow key is pressed.
 * @property {boolean} UP - Checks if the up arrow key is pressed.
 * @property {boolean} DOWN - Checks if the down arrow key is pressed.
 * @property {boolean} SPACE - Checks if the spacebar is pressed.
 * @property {boolean} letterD - Checks if the "D" key  is pressed.
 * 
 * 
 * @class Keyboard
 */

class Keyboard {

        LEFT = false;
        RIGHT = false;
        UP = false;
        DOWN = false;
        SPACE = false;
        letterD = false;

}